import { FarcasterAPIClient, RedisClient } from "@nook/common/clients";
import { PrismaClient } from "@nook/common/prisma/nook";
import { decodeCursor, encodeCursor } from "@nook/common/utils";
import { FastifyInstance } from "fastify";

const MAX_PAGE_SIZE = 25;

export class FarconService {
  private redis: RedisClient;
  private farcaster: FarcasterAPIClient;

  constructor(fastify: FastifyInstance) {
    this.redis = fastify.redis.client;
    this.farcaster = new FarcasterAPIClient();
  }

  async getAttendees(
    req: { following?: boolean; cursor?: string },
    viewerFid?: string,
  ) {
    const attendees = await this.getAttendeeFids();
    let sorted = attendees.sort((a, b) => Number(a) - Number(b));

    if (viewerFid && req.following) {
      const followingFids =
        await this.farcaster.getUserFollowingFids(viewerFid);
      sorted = sorted.filter((fid) => followingFids.data.includes(fid));
    }

    const decodedCursor = decodeCursor(req.cursor);
    if (decodedCursor?.fid) {
      sorted = sorted.filter((fid) => Number(fid) > Number(decodedCursor.fid));
    }

    const data = await this.farcaster.getUsers({ fids: sorted }, viewerFid);

    return {
      data: data.data,
      nextCursor:
        sorted.length === MAX_PAGE_SIZE
          ? encodeCursor({
              fid: sorted[sorted.length - 1],
            })
          : undefined,
    };
  }

  async getAttendeeFids() {
    const cached = await this.redis.getMembers("farcon-attendees");
    if (cached && cached.length > 0) return cached;

    const ALL_HOLDERS = VIBES_PASS_HOLDERS.concat(
      COMMUNITY_PASS_HOLDERS,
    ).concat(SUMMIT_PASS_HOLDERS);

    const fids = await this.farcaster.getUserFids({ addresses: ALL_HOLDERS });

    await this.redis.addMembers("farcon-attendees", fids.data.filter(Boolean));

    return fids.data.filter(Boolean);
  }
}

